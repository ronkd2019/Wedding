function setLanguage(lang) {
    localStorage.setItem('preferred-language', lang);
    document.documentElement.lang = lang;

    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });

    // Update switcher buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });

    // Update guests if present
    const roleMapping = {
        "Mother of the Bride": "role-mother-bride",
        "Father of the Bride": "role-father-bride",
        "Mother of the Groom": "role-mother-groom",
        "Father of the Groom": "role-father-groom",
        "Maid of Honor": "role-maid-honor",
        "Best Man": "role-best-man",
        "Bridesmaid": "role-bridesmaid",
        "Groomsman": "role-groomsman",
        "Special Guests": "role-special"
    };

    document.querySelectorAll('.polaroid .role').forEach(roleElem => {
        const originalRole = roleElem.getAttribute('data-original-role');
        if (originalRole && roleMapping[originalRole]) {
            roleElem.textContent = translations[lang][roleMapping[originalRole]];
        }
    });
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferred-language') || 'en';

    // Set active class on page load
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.getAttribute('data-lang') === savedLang) {
            btn.classList.add('active');
        }
        btn.addEventListener('click', () => {
            setLanguage(btn.getAttribute('data-lang'));
        });
    });

    setLanguage(savedLang);
});
